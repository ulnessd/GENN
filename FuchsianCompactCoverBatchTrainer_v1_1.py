# -----------------------------------------------------------------------------
# Minimal SU(1,1) Mobius implementation matching FuchsianDownstairsGINN_v2_4.
# -----------------------------------------------------------------------------


@dataclass(frozen=True)
class SU11:
    alpha: complex
    beta: complex
    word: str = ""

    def __call__(self, z: complex) -> complex:
        den = self.beta.conjugate() * z + self.alpha.conjugate()
        return (self.alpha * z + self.beta) / den

    def det(self) -> float:
        return float(abs(self.alpha) ** 2 - abs(self.beta) ** 2)

    def normalized(self, word: Optional[str] = None) -> "SU11":
        d = self.det()
        if d <= 0 or not math.isfinite(d):
            return SU11(self.alpha, self.beta, self.word if word is None else word)
        s = math.sqrt(d)
        return SU11(self.alpha / s, self.beta / s, self.word if word is None else word)

    def compose(self, other: "SU11", word: Optional[str] = None) -> "SU11":
        # self after other: self(other(z)).
        a1, b1 = self.alpha, self.beta
        a2, b2 = other.alpha, other.beta
        a = a1 * a2 + b1 * b2.conjugate()
        b = a1 * b2 + b1 * a2.conjugate()
        return SU11(a, b, self.word if word is None else word).normalized()

    def inverse(self, word: Optional[str] = None) -> "SU11":
        return SU11(self.alpha.conjugate(), -self.beta, self.word if word is None else word).normalized()

    def as_json(self) -> Dict[str, Any]:
        return {
            "type": "su11",
            "alpha": [float(self.alpha.real), float(self.alpha.imag)],
            "beta": [float(self.beta.real), float(self.beta.imag)],
            "name": self.word,
        }


def su11_from_json(label: str, data: Dict[str, Any]) -> SU11:
    ar, ai = data["alpha"]
    br, bi = data["beta"]
    return SU11(complex(float(ar), float(ai)), complex(float(br), float(bi)), label).normalized(label)


def identity() -> SU11:
    return SU11(1.0 + 0j, 0.0 + 0j, "I")


def mat_key(m: SU11, ndigits: int = 10) -> Tuple[float, float, float, float]:
    mn = m.normalized()
    return (
        round(mn.alpha.real, ndigits), round(mn.alpha.imag, ndigits),
        round(mn.beta.real, ndigits), round(mn.beta.imag, ndigits),
    )


def identity_error(m: SU11) -> float:
    pts = [0+0j, 0.13+0.07j, -0.21+0.19j, 0.32-0.27j]
    return float(max(abs(m(z) - z) for z in pts))


