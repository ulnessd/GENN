def profile_defaults(profile: str) -> Dict[str, Any]:
    if profile == "smoke":
        return {"base_genera": [2], "cover_orders": [2], "charge_modes": ["basis"], "profiles_per_pair": 1, "pairs": 500, "epochs": 3}
    if profile == "small":
        return {"base_genera": [2, 3], "cover_orders": [2, 3], "charge_modes": ["basis", "diagonal"], "profiles_per_pair": 2, "pairs": 3000, "epochs": 30}
    if profile == "balanced":
        return {"base_genera": [2, 3, 4], "cover_orders": [2, 3, 4], "charge_modes": ["basis", "diagonal", "alternating", "pair"], "profiles_per_pair": 4, "pairs": 9000, "epochs": 80}
    if profile == "wide":
        return {"base_genera": [2, 3, 4, 5], "cover_orders": [2, 3, 4, 5, 6], "charge_modes": ["basis", "diagonal", "alternating", "pair", "random"], "profiles_per_pair": 6, "pairs": 9000, "epochs": 80}
    return {"base_genera": [2], "cover_orders": [2], "charge_modes": ["basis"], "profiles_per_pair": 1, "pairs": 9000, "epochs": 80}


def main(argv: Optional[Sequence[str]] = None) -> int:
    ap = argparse.ArgumentParser(description="Compact cyclic-cover batch trainer for regular compact Fuchsian surfaces")
    ap.add_argument("--profile", default="balanced", choices=["smoke", "small", "balanced", "wide", "manual"])
    ap.add_argument("--base-genera", default="", help="comma-list override, e.g. 2,3,4")
    ap.add_argument("--cover-orders", default="", help="comma-list override, e.g. 2,3,4")
    ap.add_argument("--charge-modes", default="", help="comma-list among basis,diagonal,alternating,pair,random")
    ap.add_argument("--profiles-per-pair", type=int, default=0, help="cap charge profiles per (base genus, cover order); 0 uses profile default")
    ap.add_argument("--random-charge-count", type=int, default=4)
    ap.add_argument("--max-surfaces", type=int, default=0, help="0 means no cap")
    ap.add_argument("--max-cover-genus", type=int, default=999999)
    ap.add_argument("--maker", default="FuchsianDomainMaker_v13.py")
    ap.add_argument("--ginn-script", default="FuchsianDownstairsGINN_v2_4.py")
    ap.add_argument("--outroot", default="compact_cover_batch_runs")
    ap.add_argument("--label", default="")
    ap.add_argument("--run-ginn", action="store_true")
    ap.add_argument("--dry-run", action="store_true")
    ap.add_argument("--pairs", type=int, default=0)
    ap.add_argument("--depth", type=int, default=2)
    ap.add_argument("--max-word-ball", type=int, default=50000)
    ap.add_argument("--epochs", type=int, default=0)
    ap.add_argument("--batch-size", type=int, default=64)
    ap.add_argument("--device", default="auto")
    ap.add_argument("--pair-hidden", type=int, default=192)
    ap.add_argument("--score-hidden", type=int, default=96)
    ap.add_argument("--lr", type=float, default=1e-3)
    ap.add_argument("--patience", type=int, default=15)
    ap.add_argument("--ce-weight", type=float, default=1.0)
    ap.add_argument("--soft-distance-weight", type=float, default=0.2)
    ap.add_argument("--temperature", type=float, default=1.0)
    ap.add_argument("--candidate-chunk-size", type=int, default=8192)
    ap.add_argument("--auto-chunk-threshold-mb", type=float, default=2048.0)
    ap.add_argument("--max-generators", type=int, default=0)
    ap.add_argument("--seed", type=int, default=12345)
    args = ap.parse_args(argv)

    defaults = profile_defaults(args.profile)
    base_genera = parse_int_list(args.base_genera, defaults["base_genera"])
    cover_orders = parse_int_list(args.cover_orders, defaults["cover_orders"])
    charge_modes = [x.strip() for x in (args.charge_modes or ",".join(defaults["charge_modes"])).split(",") if x.strip()]
    profiles_per_pair = args.profiles_per_pair if args.profiles_per_pair > 0 else int(defaults["profiles_per_pair"])
    if args.pairs <= 0:
        args.pairs = int(defaults["pairs"])
    if args.epochs <= 0:
        args.epochs = int(defaults["epochs"])

    label = safe_label(args.label or f"{args.profile}_compact_covers")
    run_root = Path(args.outroot) / f"run_{now_stamp()}_{label}"
    (run_root / "surfaces").mkdir(parents=True, exist_ok=True)
    (run_root / "tables").mkdir(parents=True, exist_ok=True)

    rng = random.Random(args.seed)
    maker = load_module(args.maker, "fuchsian_domain_maker_compact_cover")
    ginn = load_module(args.ginn_script, "ginn_v24_compact_cover") if args.run_ginn and not args.dry_run else None

    surface_rows: List[Dict[str, Any]] = []
    train_rows: List[Dict[str, Any]] = []
    manifest_surfaces: List[str] = []
    serial = 0
    stop = False

    for bg in base_genera:
        for n in cover_orders:
            cover_genus = 1 + n * (bg - 1)
            if cover_genus > args.max_cover_genus:
                continue
            # Need base surface to know number of side-pairing labels.
            base = maker.make_regular_genus_surface(bg)
            num_gens = len(base.get("generators", {}))
            profiles = charge_profiles(num_gens, n, charge_modes, rng, args.random_charge_count)
            if profiles_per_pair:
                profiles = profiles[:profiles_per_pair]
            for pname, charges in profiles:
                if args.max_surfaces and serial >= args.max_surfaces:
                    stop = True
                    break
                try:
                    sj = build_compact_cover_surface(
                        maker, bg, n, pname, charges, serial,
                        max_generators=args.max_generators,
                    )
                    sid = str(sj["surface_id"])
                    manifest_surfaces.append(sid)
                    surface_rows.append(audit_surface(sj))
                    write_json(run_root / "surfaces" / f"{sid}.json", sj)
                    print(f"[surface] {sid} genus={sj.get('genus')} generators={len(sj.get('generators', {}))} tiles={len(sj.get('fundamental_domain_tiles', []))}", flush=True)
                    if args.run_ginn and not args.dry_run and ginn is not None:
                        tr = run_direct_ginn(ginn, sj, run_root, args)
                        train_rows.append({**audit_surface(sj), **tr})
                        print(f"[train] {sid} ok={tr.get('pass_ginn_training')} top1={tr.get('winning_lift_accuracy_test')} top5={tr.get('winning_lift_top5_accuracy_test')} err={str(tr.get('error',''))[:120]}", flush=True)
                    serial += 1
                except Exception as e:
                    err_sid = f"compact_cover_regg{bg}_cyc{n}_{safe_label(pname)}_n{serial:03d}"
                    row = {
                        "surface_id": err_sid,
                        "base_genus": bg,
                        "cover_order": n,
                        "charge_profile": pname,
                        "pass_surface_generation": False,
                        "error": f"{type(e).__name__}: {e}",
                    }
                    surface_rows.append(row)
                    print(f"[surface-error] {err_sid}: {row['error']}", flush=True)
                    serial += 1
            if stop:
                break
        if stop:
            break

    write_csv(run_root / "tables" / "compact_cover_surfaces.csv", surface_rows)
    if train_rows:
        write_csv(run_root / "tables" / "compact_cover_training.csv", train_rows)
    write_json(run_root / "manifest.json", {
        "program": PROGRAM,
        "version": VERSION,
        "created": datetime.now().isoformat(timespec="seconds"),
        "profile": args.profile,
        "base_genera": base_genera,
        "cover_orders": cover_orders,
        "charge_modes": charge_modes,
        "profiles_per_pair": profiles_per_pair,
        "surfaces": manifest_surfaces,
        "run_ginn": args.run_ginn,
        "dry_run": args.dry_run,
        "pairs": args.pairs,
        "depth": args.depth,
        "epochs": args.epochs,
        "max_word_ball": args.max_word_ball,
    })

    ok = sum(1 for r in train_rows if r.get("pass_ginn_training"))
    report = run_root / "compact_cover_batch_report.md"
    lines = [
        "# Compact Cover Batch Trainer Report\n\n",
        f"Program: `{PROGRAM}` v{VERSION}\n\n",
        "## Scope\n\n",
        "Smooth compact Riemann surfaces only. Each animal is a finite cyclic cover of a certified regular compact genus-g disk-polygon surface. The exported domain is an explicit triangulated union of translated base polygons in the Poincare disk. No orbifold records and no Teichmuller-space-only records are included.\n\n",
        "## Summary\n\n",
        f"- Generated/attempted surfaces: **{len(surface_rows)}**\n",
        f"- Successfully generated surfaces: **{len(manifest_surfaces)}**\n",
        f"- Run GINN: `{args.run_ginn}`\n",
        f"- Dry run: `{args.dry_run}`\n",
        f"- Training successes: `{ok}/{len(train_rows)}`\n\n",
        "## Surfaces and audit fields\n\n",
        "| surface_id | base genus | cover order | cover genus | profile | charge vector | charge onto? | GB error | RS kernel ok? | exported RS gens | canonical 2g | tiles |\n",
        "| --- | ---: | ---: | ---: | --- | --- | --- | ---: | --- | ---: | ---: | ---: |\n",
    ]
    for r in surface_rows:
        lines.append(
            f"| `{r.get('surface_id','')}` | {r.get('base_genus','')} | {r.get('cover_order','')} | {r.get('cover_genus','')} | "
            f"{r.get('charge_profile','')} | `{r.get('charge_vector_mod_n','')}` | {r.get('charge_surjective','')} | "
            f"{r.get('gauss_bonnet_error','')} | {r.get('all_rs_candidates_kernel_charge_zero','')} | "
            f"{r.get('generators','')} | {r.get('canonical_surface_group_generator_count_2g_cover','')} | {r.get('triangle_tiles','')} |\n"
        )
    lines.extend([
        "\n## Mathematical audit notes\n\n",
        "- `charge onto?` checks that the charge vector is surjective onto the cyclic quotient `Z/n`; equivalently `gcd(n, charges)=1`.\n",
        "- `GB error` is `|reported_area - 4*pi*(cover_genus-1)|`. This should be near numerical zero.\n",
        "- `RS kernel ok?` checks that every Reidemeister-Schreier candidate has zero quotient charge modulo `n`.\n",
        "- `exported RS gens` is not expected to equal `canonical 2g`. These are Reidemeister-Schreier kernel generators and may be redundant.\n",
    ])
    if train_rows:
        lines.extend([
            "\n## Training results\n\n",
            "| surface_id | pass | word ball | top1 | top5 | shortcut fraction | seconds | error |\n",
            "| --- | --- | ---: | ---: | ---: | ---: | ---: | --- |\n",
        ])
        for r in train_rows:
            lines.append(
                f"| `{r.get('surface_id','')}` | {r.get('pass_ginn_training')} | {r.get('word_ball_size','')} | "
                f"{r.get('winning_lift_accuracy_test','')} | {r.get('winning_lift_top5_accuracy_test','')} | {r.get('shortcut_fraction_test','')} | "
                f"{r.get('wall_seconds','')} | {str(r.get('error','')).replace('|','/')[:120]} |\n"
            )
    report.write_text("".join(lines), encoding="utf-8")
    print(f"[done] report={report}", flush=True)
    return 0 if all(r.get("pass_ginn_training", True) for r in train_rows) else 1


