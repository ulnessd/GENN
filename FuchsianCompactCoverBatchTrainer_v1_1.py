#!/usr/bin/env python3
r"""FuchsianCompactCoverBatchTrainer_v1_1.py

Batch generator/trainer for smooth compact finite covers of the existing
regular compact genus-g disk-polygon surfaces.

Scope
-----
* Smooth Riemann surfaces only: the base regular_g surface is torsion-free,
  and every finite-index subgroup/cover of it is torsion-free.
* Explicit Poincare-disk domains only: the cover domain is exported as a
  finite union of translated copies of the base polygon, triangulated for the
  existing GINN sampler.
* No orbifold records and no Teichmuller-space/Fenchel-Nielsen coordinate-only
  records.

Construction
------------
For a base compact surface group generated by the side-pairing labels exported
by FuchsianDomainMaker_v13.make_regular_genus_surface(g), choose an epimorphism
onto a cyclic group Z/n by assigning charges to the side-pairing generators.
The kernel is a finite-index subgroup.  A cover fundamental domain is built as

    union_{q in Z/n} R_q(P_base),

where R_q is a shortest representative word of quotient charge q.  Kernel
side-pairing generators are exported by a Reidemeister-Schreier-style formula

    h(q,x) = R_q x R_{q+phi(x)}^{-1}.

This is deliberately a first conservative compact-cover trainer: cyclic covers
only, small degrees by default, and all generated surfaces are intended as
explicit disk-domain feedstock for GENN branch-geometry experiments.
"""
