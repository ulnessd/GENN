def generator_su11_audit(sj: Dict[str, Any]) -> Dict[str, Any]:
    max_det = 0.0
    bad = 0
    for lab, g in sj.get("generators", {}).items():
        M = su11_from_json(str(lab), g)
        err = abs(M.det() - 1.0)
        max_det = max(max_det, err)
        if err > 1e-6:
            bad += 1
    return {"generator_count": len(sj.get("generators", {})), "bad_generator_count": bad, "su11_max_det_error": max_det}


